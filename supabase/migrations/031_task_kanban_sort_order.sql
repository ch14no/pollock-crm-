-- ============================================================
-- 031: タスクカンバンの列内並び替え
-- ============================================================
-- これまでカードは列（ステージ）間の移動のみ可能で、同じ列内での
-- 上下の並び替えは保存されなかった（dnd-kitのSortableContext自体は
-- 列内ドラッグに対応していたが、永続化する並び順フィールドが無く、
-- 再読み込みするとaction_date順に戻っていた）。
-- task_meta に sort_order を追加し、カードを動かした列全体を
-- 連番で一括保存する（同じ列内の並び替え・別の列への移動、いずれも
-- 移動先列を0..Nで振り直す設計）。
--
-- RLSは030で task_meta_select / task_meta_update を同一事業部メンバーに
-- 開放済みのため、新しい列（sort_order）についても追加のポリシー変更は
-- 不要（ポリシーは行単位で、既存の列も含めて同じ条件が適用される）。
-- ============================================================

ALTER TABLE public.task_meta ADD COLUMN IF NOT EXISTS sort_order INTEGER;
