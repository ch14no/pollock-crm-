-- 012: 商談の優先度カラム追加（M&A事業部要望⑦ 優先順位の設定）
--
-- ※ このマイグレーションは自動適用されません。
--    Supabaseダッシュボードの SQL Editor で人間がレビューの上、手動実行してください。
--
-- 全事業部共通の汎用機能として deals に priority を追加する。
-- 既存商談はすべて「中」として扱われる（DEFAULT 'medium'）。
-- アプリ側はカラム未適用でも動作するが、その間に設定された優先度は保存されない
-- （エラーにはならず既定の「中」で保存される）ため、コードのデプロイ後すみやかに適用すること。
--
-- 運用メモ: CHECK制約の検証で実行中はテーブルがロックされる（既存行のスキャンを伴う）。
-- 現在のdeals件数なら一瞬で終わるが、将来件数が大きく増えた場合は業務時間外の実行を推奨。
-- ============================================================

ALTER TABLE public.deals
  ADD COLUMN IF NOT EXISTS priority VARCHAR(10) NOT NULL DEFAULT 'medium'
  CHECK (priority IN ('high', 'medium', 'low'));
